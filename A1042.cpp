#include<stdio.h>
double x[8] = {0}, y[8] = {0}, ans;
int i;
int main()
{
	for(i = 0; i < 4; i++)
		scanf("%lf%lf", &x[i], &y[i]);
	if((x[0] - x[2]) * (x[0] - x[3]) < 0.0000001)
		x[i++] = x[0];
	if((x[1] - x[2]) * (x[1] - x[3]) < 0.0000001)
		x[i++] = x[1];
	if((x[2] - x[0]) * (x[2] - x[1]) < 0.0000001)
		x[i++] = x[2];
	if((x[3] - x[0]) * (x[3] - x[1]) < 0.0000001)
		x[i++] = x[3];
	i = 4;
	if((y[0] - y[2]) * (y[0] - y[3]) < 0.0000001)
		y[i++] = y[0];
	if((y[1] - y[2]) * (y[1] - y[3]) < 0.0000001)
		y[i++] = y[1];
	if((y[2] - y[0]) * (y[2] - y[1]) < 0.0000001)
		y[i++] = y[2];
	if((y[3] - y[0]) * (y[3] - y[1]) < 0.0000001)
		y[i++] = y[3];
	ans = (x[4] - x[5]) * (y[4] - y[5]);
	if(ans < 0)
		ans = -ans;
	printf("%.2lf", ans);
	return 0;
}
